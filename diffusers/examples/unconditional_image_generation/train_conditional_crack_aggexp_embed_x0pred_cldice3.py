def collate_fn(examples):
    return {
        "image": torch.stack([example["image"] for example in examples]),
        "mask": torch.stack([example["mask"] for example in examples]),
        "aggregate_mask": torch.stack([example["aggregate_mask"] for example in examples]),
        "filename": [example["filename"] for example in examples],
        "expansion": torch.stack([example["expansion"] for example in examples]),
        "aggregate_class": torch.stack([example["aggregate_class"] for example in examples]),
        "combo_id": torch.stack([example["combo_id"] for example in examples]),
    }


def prepare_validation_batch(validation_dataset, eval_batch_size):
    if len(validation_dataset) == 0:
        return None
    subset = Subset(validation_dataset, range(min(eval_batch_size, len(validation_dataset))))
    validation_loader = DataLoader(subset, batch_size=len(subset), shuffle=False, collate_fn=collate_fn)
    return next(iter(validation_loader))


def build_validation_grid(concrete_images, aggregate_masks, gt_masks, pred_masks, binary_masks):
    rows = []
    for concrete_image, aggregate_mask, gt_mask, pred_mask, binary_mask in zip(
        concrete_images, aggregate_masks, gt_masks, pred_masks, binary_masks
    ):
        concrete_image = (concrete_image.clamp(-1, 1) + 1.0) / 2.0
        aggregate_mask = aggregate_mask.repeat(3, 1, 1)
        gt_mask_rgb = gt_mask.repeat(3, 1, 1)
        pred_mask_rgb = pred_mask.repeat(3, 1, 1)
        binary_mask_rgb = binary_mask.repeat(3, 1, 1)
        rows.append(torch.cat([concrete_image, aggregate_mask, gt_mask_rgb, pred_mask_rgb, binary_mask_rgb], dim=2))
    return make_grid(rows, nrow=1)


def dice_loss(pred_mask_prob, target_mask_prob, eps=1e-6):
    pred = pred_mask_prob.reshape(pred_mask_prob.shape[0], -1)
    target = target_mask_prob.reshape(target_mask_prob.shape[0], -1)
    intersection = (pred * target).sum(dim=1)
    denominator = pred.sum(dim=1) + target.sum(dim=1)
    dice = (2.0 * intersection + eps) / (denominator + eps)
    return 1.0 - dice.mean()


def soft_erode(mask_prob):
    eroded_h = -F.max_pool2d(-mask_prob, kernel_size=(3, 1), stride=1, padding=(1, 0))
    eroded_w = -F.max_pool2d(-mask_prob, kernel_size=(1, 3), stride=1, padding=(0, 1))
    return torch.minimum(eroded_h, eroded_w)


def soft_dilate(mask_prob):
    return F.max_pool2d(mask_prob, kernel_size=3, stride=1, padding=1)


def soft_dilate_k(mask_prob, kernel_size):
    pad = kernel_size // 2
    return F.max_pool2d(mask_prob, kernel_size=kernel_size, stride=1, padding=pad)


def soft_open(mask_prob):
    return soft_dilate(soft_erode(mask_prob))


def soft_skeletonize(mask_prob, iterations):
    opened = soft_open(mask_prob)
    skeleton = F.relu(mask_prob - opened)
    for _ in range(iterations):
        mask_prob = soft_erode(mask_prob)
        opened = soft_open(mask_prob)
        delta = F.relu(mask_prob - opened)
        skeleton = skeleton + F.relu(delta - skeleton * delta)
    return skeleton


def soft_cldice_loss(pred_mask_prob, target_mask_prob, iterations=10, eps=1e-6):
    pred_skeleton = soft_skeletonize(pred_mask_prob, iterations)
    target_skeleton = soft_skeletonize(target_mask_prob, iterations)

    pred = pred_mask_prob.reshape(pred_mask_prob.shape[0], -1)
    target = target_mask_prob.reshape(target_mask_prob.shape[0], -1)
    pred_skel = pred_skeleton.reshape(pred_skeleton.shape[0], -1)
    target_skel = target_skeleton.reshape(target_skeleton.shape[0], -1)

    topology_precision = (pred_skel * target).sum(dim=1) / (pred_skel.sum(dim=1) + eps)
    topology_sensitivity = (target_skel * pred).sum(dim=1) / (target_skel.sum(dim=1) + eps)
    cldice = (2.0 * topology_precision * topology_sensitivity + eps) / (
        topology_precision + topology_sensitivity + eps
    )
    return 1.0 - cldice.mean()


def masks_to_probability(pred_x0, clean_mask):
    pred_prob = ((pred_x0.clamp(-1, 1) + 1.0) / 2.0).float()
    target_prob = ((clean_mask.clamp(-1, 1) + 1.0) / 2.0).float()
    return pred_prob, target_prob


def aggregate_localization_penalty(pred_prob, agg_prob, dilate_kernel):
    if dilate_kernel >= 3:
        agg_dilated = soft_dilate_k(agg_prob, dilate_kernel)
    else:
        agg_dilated = agg_prob
    out_of_support = (1.0 - agg_dilated).clamp(0.0, 1.0)
    return (pred_prob * out_of_support).mean()


@torch.no_grad()
def run_validation(unet, noise_scheduler, validation_batch, epoch, args, accelerator, weight_dtype):
    if validation_batch is None:
        return

    unet.eval()

    concrete_image = validation_batch["image"].to(device=accelerator.device, dtype=weight_dtype)
    gt_mask = validation_batch["mask"].to(device=accelerator.device, dtype=weight_dtype)
    aggregate_mask = validation_batch["aggregate_mask"].to(device=accelerator.device, dtype=weight_dtype)
    combo_id = validation_batch["combo_id"].to(device=accelerator.device, dtype=torch.long)
    null_id = torch.full_like(combo_id, args.null_class_id)

    validation_generator = torch.Generator(device=accelerator.device)
    validation_generator.manual_seed(args.validation_seed)
    mask = torch.randn(
        (concrete_image.shape[0], 1, args.resolution, args.resolution),
        device=accelerator.device,
        dtype=weight_dtype,
        generator=validation_generator,
    )

    cfg_scale = args.validation_guidance_scale
    use_cfg = cfg_scale > 0.0

    noise_scheduler.set_timesteps(args.ddpm_num_inference_steps)
    for t in noise_scheduler.timesteps:
        timestep_batch = torch.full((mask.shape[0],), int(t), device=mask.device, dtype=torch.long)
        model_input = torch.cat([mask, concrete_image, aggregate_mask], dim=1)
        if use_cfg:
            pred_cond = unet(model_input, timestep_batch, class_labels=combo_id).sample
            pred_uncond = unet(model_input, timestep_batch, class_labels=null_id).sample
            model_pred = pred_uncond + cfg_scale * (pred_cond - pred_uncond)
        else:
            model_pred = unet(model_input, timestep_batch, class_labels=combo_id).sample
        mask = noise_scheduler.step(model_pred, t, mask).prev_sample

    pred_mask = ((mask.clamp(-1, 1) + 1.0) / 2.0).float().cpu()
    gt_mask = ((gt_mask.clamp(-1, 1) + 1.0) / 2.0).float().cpu()
    aggregate_mask = ((aggregate_mask.clamp(-1, 1) + 1.0) / 2.0).float().cpu()
    concrete_image = concrete_image.float().cpu()
    binary_mask = (pred_mask > args.threshold).float()

    grid = build_validation_grid(concrete_image, aggregate_mask, gt_mask, pred_mask, binary_mask)

    sample_dir = os.path.join(args.output_dir, "samples")
    os.makedirs(sample_dir, exist_ok=True)
    sample_path = os.path.join(sample_dir, f"epoch_{epoch:04d}.png")
    save_image(grid, sample_path)

    if args.logger == "tensorboard":
        if is_accelerate_version(">=", "0.17.0.dev0"):
            tracker = accelerator.get_tracker("tensorboard", unwrap=True)
        else:
            tracker = accelerator.get_tracker("tensorboard")
        tracker.add_image("validation_samples", grid, epoch)
    elif args.logger == "wandb":
        import wandb

        accelerator.get_tracker("wandb").log(
            {
                "validation_samples": wandb.Image(
                    sample_path,
                    caption="Columns: concrete image | aggregate mask | gt mask | generated mask | thresholded mask",
                ),
                "epoch": epoch,
            },
            step=epoch,
        )

    unet.train()


