def parse_int_label(value, label_name):
    try:
        label = int(float(str(value).strip()))
    except ValueError as exc:
        raise ValueError(f"{label_name} must be an integer-like value, got {value!r}") from exc
    return label


def parse_expansion_label(value):
    expansion = parse_int_label(value, "expansion")
    if expansion not in (0, 1):
        raise ValueError(f"expansion must be 0 or 1 for combo embedding, got {expansion}")
    return expansion


def parse_aggregate_class(value):
    aggregate_class = parse_int_label(value, "aggregate_class")
    if aggregate_class not in (0, 1, 2):
        raise ValueError(f"aggregate_class must be 0, 1, or 2, got {aggregate_class}")
    return aggregate_class


def parse_combo_id(value):
    combo_id = parse_int_label(value, "combo_id")
    if combo_id not in range(6):
        raise ValueError(f"combo_id must be in [0, 5], got {combo_id}")
    return combo_id


class CrackMaskConditionDataset(Dataset):
    def __init__(
        self,
        image_dir,
        mask_dir,
        aggregate_mask_dir,
        metadata_csv,
        resolution,
        expansion_min,
        expansion_max,
        center_crop=False,
        random_flip=False,
        random_rotate90=False,
        target_dilate_kernel=1,
    ):
        self.image_dir = Path(image_dir)
        self.mask_dir = Path(mask_dir)
        self.aggregate_mask_dir = Path(aggregate_mask_dir)
        self.resolution = resolution
        self.center_crop = center_crop
        self.random_flip = random_flip
        self.random_rotate90 = random_rotate90
        if target_dilate_kernel < 1 or target_dilate_kernel % 2 == 0:
            raise ValueError(f"target_dilate_kernel must be a positive odd integer, got {target_dilate_kernel}")
        self.target_dilate_kernel = target_dilate_kernel
        self.expansion_min = expansion_min
        self.expansion_max = expansion_max

        if expansion_max <= expansion_min:
            raise ValueError("--expansion_max must be greater than --expansion_min.")

        self.records = []
        with open(metadata_csv, newline="") as csv_file:
            reader = csv.DictReader(csv_file)
            for row in reader:
                required_columns = {"filename", "expansion", "aggregate_class", "combo_id"}
                missing_columns = required_columns - set(reader.fieldnames or [])
                if missing_columns:
                    raise ValueError(
                        "metadata.csv must be generated by write_exp_agg_labels_csv.py and contain "
                        f"columns: {', '.join(sorted(required_columns))}. "
                        f"Missing: {', '.join(sorted(missing_columns))}"
                    )

                filename = row["filename"].strip()
                expansion = parse_expansion_label(row["expansion"])
                aggregate_class = parse_aggregate_class(row["aggregate_class"])
                combo_id = parse_combo_id(row["combo_id"])
                expected_combo_id = aggregate_class * 2 + expansion
                if combo_id != expected_combo_id:
                    raise ValueError(
                        f"Invalid combo_id for {filename}: got {combo_id}, expected "
                        f"aggregate_class * 2 + expansion = {expected_combo_id}"
                    )
                self.records.append(
                    {
                        "filename": filename,
                        "expansion": expansion,
                        "aggregate_class": aggregate_class,
                        "combo_id": combo_id,
                    }
                )

        if len(self.records) == 0:
            raise ValueError("No samples were found in metadata.csv.")

    def __len__(self):
        return len(self.records)

    def _paired_spatial_transform(self, image, mask, aggregate_mask):
        image = TF.resize(image, self.resolution, interpolation=InterpolationMode.BILINEAR)
        mask = TF.resize(mask, self.resolution, interpolation=InterpolationMode.NEAREST)
        aggregate_mask = TF.resize(aggregate_mask, self.resolution, interpolation=InterpolationMode.NEAREST)

        if self.center_crop:
            image = TF.center_crop(image, [self.resolution, self.resolution])
            mask = TF.center_crop(mask, [self.resolution, self.resolution])
            aggregate_mask = TF.center_crop(aggregate_mask, [self.resolution, self.resolution])
        else:
            top, left, height, width = RandomCrop.get_params(image, output_size=(self.resolution, self.resolution))
            image = TF.crop(image, top, left, height, width)
            mask = TF.crop(mask, top, left, height, width)
            aggregate_mask = TF.crop(aggregate_mask, top, left, height, width)

        if self.random_flip and torch.rand(1).item() < 0.5:
            image = TF.hflip(image)
            mask = TF.hflip(mask)
            aggregate_mask = TF.hflip(aggregate_mask)

        if self.random_rotate90:
            k = int(torch.randint(0, 4, (1,)).item())
            if k != 0:
                angle = 90.0 * k
                image = TF.rotate(image, angle, interpolation=InterpolationMode.BILINEAR)
                mask = TF.rotate(mask, angle, interpolation=InterpolationMode.NEAREST)
                aggregate_mask = TF.rotate(aggregate_mask, angle, interpolation=InterpolationMode.NEAREST)

        return image, mask, aggregate_mask

    def __getitem__(self, index):
        record = self.records[index]
        image_path = self.image_dir / record["filename"]
        mask_path = self.mask_dir / record["filename"]
        aggregate_mask_path = self.aggregate_mask_dir / record["filename"]

        if not image_path.exists():
            raise FileNotFoundError(f"Missing image file: {image_path}")
        if not mask_path.exists():
            raise FileNotFoundError(f"Missing mask file: {mask_path}")
        if not aggregate_mask_path.exists():
            raise FileNotFoundError(f"Missing aggregate mask file: {aggregate_mask_path}")

        image = Image.open(image_path).convert("RGB")
        mask = Image.open(mask_path).convert("L")
        aggregate_mask = Image.open(aggregate_mask_path).convert("L")
        image, mask, aggregate_mask = self._paired_spatial_transform(image, mask, aggregate_mask)

        image = TF.to_tensor(image)
        image = image * 2.0 - 1.0

        # Continuous antialiased mask in [0, 1]; no binary thresholding so edge gradients survive.
        mask = TF.to_tensor(mask)
        # Dilate the GT crack so a 1-2px line survives the UNet bottleneck. Inference can erode/skeletonize back.
        if self.target_dilate_kernel >= 3:
            pad = self.target_dilate_kernel // 2
            mask = F.max_pool2d(
                mask.unsqueeze(0), kernel_size=self.target_dilate_kernel, stride=1, padding=pad
            ).squeeze(0)
        mask = mask * 2.0 - 1.0

        # Aggregate mask is already binary in source; keep it binary in [-1, 1].
        aggregate_mask = TF.to_tensor(aggregate_mask)
        aggregate_mask = (aggregate_mask > 0.5).float()
        aggregate_mask = aggregate_mask * 2.0 - 1.0

        return {
            "image": image,
            "mask": mask,
            "aggregate_mask": aggregate_mask,
            "filename": record["filename"],
            "expansion": torch.tensor(record["expansion"], dtype=torch.long),
            "aggregate_class": torch.tensor(record["aggregate_class"], dtype=torch.long),
            "combo_id": torch.tensor(record["combo_id"], dtype=torch.long),
        }


